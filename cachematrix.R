## i st function will create a matrix from user and all the functions to get or 
## set data or get or set inverse

## this is a make cache matrix function as name describe it makes basic matrix and 
## required functions

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


